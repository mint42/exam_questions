#include <stdio.h>

void	print_doublon(int *a, int na, int *b, int nb)
{
	int		init;

	init = 1;
	while (na && nb)
	{
		if (*a > *b)
		{
			++b;
			--nb;
		}
		else if (*a < *b)
		{
			++a;
			--na;
		}
		else if (*a == *b)
		{
			if (!init)
				printf(" ");
			printf("%d", *a);
			init = 0;
			--na;
			--nb;
			++a;
			++b;
		}
	}
	printf("\n");
}

